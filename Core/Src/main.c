// 数据帧处理函数
int process_frame(uint8_t *rxbuffer)
{
  if (rxbuffer[0] == 0x01 && rxbuffer[1] == 0x03 && rxbuffer[2] == 0x02)
  {
    uint16_t data = (rxbuffer[3] << 8) | rxbuffer[4];
    if (data <= 2000)
    {
      return data;
    }
    else
    {
      return -1;
    }
  }
  return -1;
}

